import java.util.*;

public class Sample {  
  public static void main(String[] args) {
    List<Integer> ages = Arrays.asList(10, 11, 14, 20);
    
    System.out.println(
      ages.stream()
        .reduce(0, (total, age) -> total + age));  
  }
}
